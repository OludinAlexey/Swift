//Задача 1: Счётчик времени Напишите программу, использующую цикл while, которая будет считать время в секундах от 0 до 60, а затем начнёт счёт заново.
//
import Foundation
var i: Int = 0
while i <= 60 {
    if i != 60 {
        i += 1
    } else {
        i = 0
    }
    print(i)
    sleep(1)
}


