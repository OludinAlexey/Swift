//Задача 2: Поиск делителей числа Используйте цикл while, чтобы найти все делители заданного числа и вывести их на экран.

let number: UInt = UInt.random(in: 0...1000)
print("Делители для числа \(number):")
var j: UInt = 1
while j <= number {
    if number % j == 0 {
        print(j)
    }
    j += 1
}
